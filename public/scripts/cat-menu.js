let fCat = document.querySelector(".first-cat");
let sCat = document.querySelector(".second-cat");
let tCat = document.querySelector(".third-cat");
function mapCat() {
  fCat.innerHTML = "";
  sCat.innerHTML = "";
  tCat.innerHTML = "";
  categories.map((main) => {
    let d1 = document.createElement("div");
    let first = document.createElement("h3");
    first.textContent = main.cat1;

    if (first.innerHTML !== "") {
      let next = document.createElement("img");
      next.classList.add("next");
      next.classList.add("icon");
      next.src = "/assets/next.png";
      d1.append(first, next);
      fCat.append(d1);
    }
    // }
    first.addEventListener("click", () => {
      localStorage.setItem("catData", JSON.stringify(main.sub1));
      location.href = `/courses/${main.cat1.toLowerCase()}`;
    });
    first.addEventListener("mouseenter", () => {
      first.classList.add("blue");
      sCat.style.display = "block";
      sCat.innerHTML = "";
      main.sub1.map((sec) => {
        let d2 = document.createElement("div");
        let second = document.createElement("h3");
        second.textContent = sec.cat1;
        let next = document.createElement("img");
        next.classList.add("next");
        next.classList.add("icon");
        next.src = "/assets/next.png";
        d2.append(second, next);
        sCat.append(d2);
        second.addEventListener("click", () => {
          localStorage.setItem("catData", JSON.stringify(main.sub1));
          location.href = `/courses/${main.cat1.toLowerCase()}/${sec.cat1.toLowerCase()}`;
        });
        second.addEventListener("mouseenter", () => {
          second.classList.add("blue");
          tCat.innerHTML = "";
          tCat.style.display = "block";
          sec.sub1.map((thi) => {
            let d3 = document.createElement("div");
            let third = document.createElement("h3");
            third.textContent = thi;
            d3.append(third);
            tCat.append(d3);
            third.addEventListener("click", () => {
              localStorage.setItem("catData", JSON.stringify(main.sub1));
              location.href = `/courses/${main.cat1.toLowerCase()}/${sec.cat1.toLowerCase()}/${thi.toLowerCase()}`;
            });
          });
        });
        second.addEventListener("mouseleave", () => {
          second.classList.remove("blue");
        });
      });
    });
    first.addEventListener("mouseleave", () => {
      first.classList.remove("blue");
    });
  });
}
let catToggle = document.querySelector("#categories");

catToggle.addEventListener("mouseenter", () => {
  document.querySelector(".cat-menu").classList.add("cat-active");
  mapCat();
});
document.querySelector(".cat-menu").addEventListener("mouseleave", () => {
  document.querySelector(".cat-menu").classList.remove("cat-active");
  sCat.style.display = "none";
  tCat.style.display = "none";
});
catToggle.onclick = mapCat();
document.querySelector(".cat-menu").addEventListener("mouseenter", () => {
  document.querySelector(".cat-menu").classList.add("cat-active");
});
